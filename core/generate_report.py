def create_histogram_chart(score_distribution: Dict[str, int], width: float = 400, height: float = 200) -> Drawing:
    """Create a score distribution histogram."""
    drawing = Drawing(width, height)
    
    # Data preparation
    labels = list(score_distribution.keys())
    values = list(score_distribution.values())
    
    if not values or sum(values) == 0:
        return drawing
    
    # Create bar chart
    chart = VerticalBarChart()
    chart.x = 50
    chart.y = 30
    chart.width = width - 80
    chart.height = height - 60
    
    chart.data = [values]
    chart.categoryAxis.categoryNames = labels
    chart.categoryAxis.labels.fontName = 'Helvetica'
    chart.categoryAxis.labels.fontSize = 9
    chart.categoryAxis.labels.angle = 0
    
    chart.valueAxis.valueMin = 0
    chart.valueAxis.valueMax = max(values) * 1.2 if values else 10
    chart.valueAxis.labels.fontName = 'Helvetica'
    chart.valueAxis.labels.fontSize = 9
    
    # Bar colors based on score range
    bucket_colors = [
        SCORE_COLORS['poor'],
        SCORE_COLORS['needs_work'],
        SCORE_COLORS['good'],
        SCORE_COLORS['excellent'],
    ]
    
    chart.bars[0].fillColor = HexColor(DEFAULT_PRIMARY_COLOR)
    for i, label in enumerate(labels):
        if '0' in label or '25' in label or '49' in label:
            chart.bars[0].fillColor = SCORE_COLORS['poor']
        elif '50' in label or '69' in label:
            chart.bars[0].fillColor = SCORE_COLORS['needs_work']
    
    # Apply colors per bar
    for i in range(len(values)):
        if i < len(bucket_colors):
            chart.bars[0].fillColor = bucket_colors[min(i, len(bucket_colors)-1)]
    
    drawing.add(chart)
    return drawing


