    parser.add_argument('--language', default='en', choices=['en', 'nl', 'ro'], help='Report language')
    parser.add_argument('--page-size', default='A4', choices=['A4', 'LETTER'], help='Page size')
    
    # Preview mode
    parser.add_argument('--preview', action='store_true', help='Generate preview (first 5 pages only)')
    
    # Debug
    parser.add_argument('--verbose', '-v', action='store_true', help='Verbose output')
    
    args = parser.parse_args()
    
