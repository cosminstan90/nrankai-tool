    def _generate_key_findings(self) -> List[str]:
        """Generate key findings text from statistics."""
        findings = []
        stats = self.statistics
        
        if 'excellent_count' in stats:
            findings.append(f"{stats['excellent_count']} pages scored 'Excellent' (85+)")
        
        if 'poor_count' in stats and stats['poor_count'] > 0:
            findings.append(f"{stats['poor_count']} pages need immediate attention (below 50)")
        
        # Issue category findings
        if 'category_breakdown' in stats:
            top_categories = sorted(stats['category_breakdown'].items(), key=lambda x: x[1], reverse=True)[:3]
            for cat, count in top_categories:
                findings.append(f"{count} issues related to '{cat}'")
        
        # Severity findings
        if 'severity_breakdown' in stats:
            critical = stats['severity_breakdown'].get('critical', 0)
            if critical > 0:
                findings.append(f"{critical} critical issues require urgent action")
            
            major = stats['severity_breakdown'].get('major', 0) + stats['severity_breakdown'].get('high', 0)
            if major > 0:
                findings.append(f"{major} major issues identified")
        
        # Cross-ref insights
        if self.cross_ref_data:
            analysis = self.cross_ref_data.get('cross_reference_analysis', {})
            llm = analysis.get('llm_analysis', {})
            
            # Site-wide issues
            site_issues = llm.get('site_wide_issues', [])
            if site_issues:
                findings.append(f"{len(site_issues)} site-wide issues identified")
            
            # Content gaps
            gaps = llm.get('content_gaps', [])
            if gaps:
                high_priority = [g for g in gaps if g.get('priority') == 'high']
                if high_priority:
                    findings.append(f"{len(high_priority)} high-priority content gaps identified")
        
        return findings
    
