    def load_data(self):
        """Load all audit data from JSON files."""
        logger.info(f"Loading data for {self.website} - {self.audit_type}")
        
        # Load cross-reference data if available
        if self.cross_ref_file and self.cross_ref_file.exists():
            try:
                with open(self.cross_ref_file, 'r', encoding='utf-8') as f:
                    self.cross_ref_data = json.load(f)
                logger.info(f"Loaded cross-reference data from {self.cross_ref_file}")
            except Exception as e:
                logger.warning(f"Could not load cross-reference file: {e}")
        
        # Load individual page results
        if self.results_dir and self.results_dir.exists():
            json_files = sorted(self.results_dir.glob("*.json"))
            
            for json_file in json_files:
                # Skip cross-reference and meta files
                if 'cross_reference' in json_file.name or json_file.name.startswith('_'):
                    continue
                
                try:
                    with open(json_file, 'r', encoding='utf-8') as f:
                        data = json.load(f)
                    
                    # Extract score from filename or data
                    score = extract_score_from_filename(json_file.name)
                    if score is None:
                        score = self._extract_score_from_data(data)
                    
                    # Get URL
                    url = data.get('url', get_url_from_filename(json_file.name))
                    
                    # Extract issues
                    issues = self._extract_issues_from_data(data)
                    
                    # Extract quick wins
                    quick_wins = self._extract_quick_wins_from_data(data)
                    
                    page_result = {
                        'filename': json_file.name,
                        'url': url,
                        'score': score,
                        'data': data,
                        'issues': issues,
                        'quick_wins': quick_wins,
                    }
                    
                    # Add extra metrics if available
                    if self.audit_config.get('extra_paths'):
                        for key, path in self.audit_config['extra_paths'].items():
                            page_result[key] = get_nested_value(data, path)
                    
                    self.page_results.append(page_result)
                    self.all_issues.extend(issues)
                    
                except Exception as e:
                    logger.warning(f"Could not load {json_file}: {e}")
            
            logger.info(f"Loaded {len(self.page_results)} page results")
        
        # Calculate statistics
        self._calculate_statistics()
    
    @staticmethod
    def _safe_int(value) -> Optional[int]:
        """Safely convert a value to int, handling strings like '58' and '/100' suffixes."""
        if value is None:
            return None
        if isinstance(value, (int, float)):
            return int(value)
        if isinstance(value, str):
            cleaned = value.strip().split('/')[0].strip()
            try:
                return int(float(cleaned))
            except (ValueError, TypeError):
                return None
        return None

    def _extract_score_from_data(self, data: dict) -> Optional[int]:
        """Extract score from JSON data based on audit type."""
        config = self.audit_config

        if config['type'] in ('score', 'kantar'):
            score_path = config.get('score_path', [])
            score = get_nested_value(data, score_path)
            result = self._safe_int(score)
            if result is not None:
                return result
        elif config['type'] == 'violation':
            violations = get_nested_value(data, config.get('violations_path', []), [])
            # For violations, return inverted score (0 violations = 100)
            return max(0, 100 - len(violations) * 10) if isinstance(violations, list) else None
        elif config['type'] == 'error_count':
            errors = get_nested_value(data, config.get('errors_path', []), [])
            return max(0, 100 - len(errors) * 5) if isinstance(errors, list) else None

        return None
    
    def _extract_issues_from_data(self, data: dict) -> List[dict]:
        """Extract issues from JSON data."""
        config = self.audit_config
        
        if config['type'] == 'score':
            issues_path = config.get('issues_path', ['issues'])
            return get_nested_value(data, issues_path, []) or []
        elif config['type'] == 'violation':
            return get_nested_value(data, config.get('violations_path', []), []) or []
        elif config['type'] == 'error_count':
            return get_nested_value(data, config.get('errors_path', []), []) or []
        
        return []
    
    def _extract_quick_wins_from_data(self, data: dict) -> List[str]:
        """Extract quick wins from JSON data."""
        config = self.audit_config
        quick_wins_path = config.get('quick_wins_path', [])
        
        if quick_wins_path:
            wins = get_nested_value(data, quick_wins_path, [])
            if isinstance(wins, list):
                return [str(w) for w in wins if w]
        
        return []
    
    def _calculate_statistics(self):
        """Calculate aggregate statistics from page results."""
        scores = [p['score'] for p in self.page_results if p['score'] is not None]
        
        if scores:
            self.statistics = {
                'total_pages': len(self.page_results),
                'average_score': round(sum(scores) / len(scores), 1),
                'median_score': sorted(scores)[len(scores) // 2],
                'min_score': min(scores),
                'max_score': max(scores),
                'excellent_count': len([s for s in scores if s >= 85]),
                'good_count': len([s for s in scores if 70 <= s < 85]),
                'needs_work_count': len([s for s in scores if 50 <= s < 70]),
                'poor_count': len([s for s in scores if s < 50]),
                'total_issues': len(self.all_issues),
            }
            
            # Score distribution
            self.statistics['score_distribution'] = {
                '0-49': self.statistics['poor_count'],
                '50-69': self.statistics['needs_work_count'],
                '70-84': self.statistics['good_count'],
                '85-100': self.statistics['excellent_count'],
            }
            
            # Issue severity breakdown
            severity_counts = Counter()
            category_counts = Counter()
            
            for issue in self.all_issues:
                severity = issue.get('severity', issue.get('priority', 'minor'))
                severity_counts[str(severity).lower()] += 1
                
                category = issue.get('category', issue.get('type', 'general'))
                category_counts[str(category).lower()] += 1
            
            self.statistics['severity_breakdown'] = dict(severity_counts)
            self.statistics['category_breakdown'] = dict(category_counts)
        else:
            self.statistics = {
                'total_pages': len(self.page_results),
                'average_score': 0,
                'total_issues': len(self.all_issues),
            }
        
        # Use cross-ref data if available
        if self.cross_ref_data:
            cross_stats = self.cross_ref_data.get('cross_reference_analysis', {}).get('statistics', {})
            if cross_stats:
                self.statistics.update({
                    'average_score': cross_stats.get('average_score', self.statistics.get('average_score', 0)),
                    'median_score': cross_stats.get('median_score', self.statistics.get('median_score', 0)),
                })
    
    def generate(self, output_path: str = None):
        """Generate the PDF report."""
        if not output_path:
            timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
            output_path = self.output_dir / f"{self.website}_{self.audit_type}_report_{timestamp}.pdf"
        
        output_path = Path(output_path)
        output_path.parent.mkdir(parents=True, exist_ok=True)
        
        logger.info(f"Generating PDF report: {output_path}")
        
        # Create document
        doc = SimpleDocTemplate(
            str(output_path),
            pagesize=self.page_size,
            rightMargin=2*cm,
            leftMargin=2*cm,
            topMargin=2.5*cm,
            bottomMargin=2.5*cm,
        )
        
        # Build story (content)
        story = []
        
        # Cover page
        story.extend(self._build_cover_page())
        story.append(PageBreak())
        
        # Executive summary
        story.extend(self._build_executive_summary())
        story.append(PageBreak())
        
        # Score distribution
        story.extend(self._build_score_distribution())
        
        if self.preview:
            # Preview mode: only first 5 pages
            story.append(PageBreak())
            story.extend(self._build_detailed_results(limit=5))
        else:
            story.append(PageBreak())
            
            # Detailed results
            story.extend(self._build_detailed_results())
            story.append(PageBreak())
            
            # Methodology
            story.extend(self._build_methodology())
            
            # Appendix (optional)
            if self.include_appendix:
                story.append(PageBreak())
                story.extend(self._build_appendix())
        
        # Build PDF with custom page handling
        doc.build(story, onFirstPage=self._add_page_header_footer, 
                  onLaterPages=self._add_page_header_footer)
        
        logger.info(f"PDF report generated: {output_path}")
        return output_path
    
    def _add_page_header_footer(self, canvas, doc):
        """Add header and footer to each page."""
        canvas.saveState()
        
        page_width, page_height = self.page_size
        
        # Header line
        canvas.setStrokeColor(HexColor("#e5e7eb"))
        canvas.setLineWidth(0.5)
        canvas.line(2*cm, page_height - 2*cm, page_width - 2*cm, page_height - 2*cm)
        
        # Header text
        canvas.setFillColor(HexColor("#6b7280"))
        canvas.setFont("Helvetica", 8)
        canvas.drawString(2*cm, page_height - 1.7*cm, f"{self.website} | {self.audit_config['name']}")
        canvas.drawRightString(page_width - 2*cm, page_height - 1.7*cm, f"{self.t['page']} {doc.page}")
        
        # Footer line
        canvas.line(2*cm, 2*cm, page_width - 2*cm, 2*cm)
        
        # Footer text
        canvas.drawCentredString(page_width / 2, 1.5*cm, f"{self.t['generated_by']} | {self.t['confidential']}")
        
        canvas.restoreState()
    
