    def _build_detailed_results(self, limit: int = None) -> List:
        """Build detailed per-page results."""
        story = []
        page_width = self.page_size[0] - 4*cm
        
        story.append(Paragraph(self.t['detailed_results'], self.styles['SectionHeading']))
        
        # Sort pages: top_n highest, then bottom 10
        sorted_pages = sorted([p for p in self.page_results if p['score'] is not None],
                             key=lambda x: x['score'], reverse=True)
        
        if limit:
            pages_to_show = sorted_pages[:limit]
        else:
            top_pages = sorted_pages[:self.top_n]
            bottom_pages = sorted_pages[-10:] if len(sorted_pages) > self.top_n else []
            # Remove duplicates if bottom overlaps with top
            bottom_urls = {p['url'] for p in top_pages}
            bottom_pages = [p for p in bottom_pages if p['url'] not in bottom_urls]
            pages_to_show = top_pages + bottom_pages
        
        for i, page in enumerate(pages_to_show):
            page_content = self._build_page_result(page, page_width)
            
            # Keep page content together if possible
            story.append(KeepTogether(page_content))
            
            if i < len(pages_to_show) - 1:
                story.append(Spacer(1, 0.8*cm))
        
        return story
    
    def _build_page_result(self, page: Dict, page_width: float) -> List:
        """Build content for a single page result."""
        content = []
        
        # Page URL/title
        content.append(Paragraph(
            escape_xml(truncate_text(page['url'], 80)),
            self.styles['PageURL']
        ))
        
        # Score bar
        if page['score'] is not None:
            score_bar = HorizontalScoreBar(page['score'], width=page_width * 0.6)
            content.append(score_bar)
            content.append(Spacer(1, 0.3*cm))
        
        # Extra metrics (for score-based audits)
        extra_metrics = self.audit_config.get('extra_metrics', [])
        if extra_metrics:
            metrics_data = []
            for key, label in extra_metrics:
                value = page.get(key)
                if value is not None:
                    metrics_data.append(f"{label}: {value}")
            
            if metrics_data:
                content.append(Paragraph(
                    " | ".join(metrics_data),
                    ParagraphStyle('Metrics', parent=self.styles['ReportBody'], fontSize=9, textColor=HexColor("#6b7280"))
                ))
                content.append(Spacer(1, 0.3*cm))
        
        # Issues table
        issues = page.get('issues', [])
        if issues:
            content.append(Paragraph(f"<b>{self.t['issues']}</b> ({len(issues)})", self.styles['ReportBody']))
            
            issues_data = [[self.t['severity'], self.t['issue'], self.t['recommendation']]]
            
            for issue in issues[:8]:  # Limit to 8 issues per page
                severity = issue.get('severity', issue.get('priority', 'minor'))
                issue_text = issue.get('issue', issue.get('description', issue.get('fragment', '')))
                recommendation = issue.get('recommendation', issue.get('mitigation_suggestion', ''))
                
                issues_data.append([
                    str(severity).capitalize(),
                    truncate_text(str(issue_text), 50),
                    truncate_text(str(recommendation), 50),
                ])
            
            issues_table = Table(issues_data, colWidths=[2*cm, 6*cm, 6*cm])
            issues_table.setStyle(self._get_issues_table_style())
            content.append(issues_table)
        
        # Quick wins
        quick_wins = page.get('quick_wins', [])
        if quick_wins:
            content.append(Spacer(1, 0.3*cm))
            content.append(Paragraph(f"<b>{self.t['quick_wins']}:</b>", self.styles['ReportBody']))
            for win in quick_wins[:3]:
                content.append(Paragraph(f"• {escape_xml(win)}", self.styles['KeyFinding']))
        
        return content
    
