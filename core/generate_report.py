def get_nested_value(data: dict, path: List[str], default=None):
    """Safely get a nested dictionary value using a path list."""
    current = data
    for key in path:
        if isinstance(current, dict) and key in current:
            current = current[key]
        else:
            return default
    return current


def extract_score_from_filename(filename: str) -> Optional[int]:
    """Extract numerical score from filename prefix (e.g., '085_page.json' -> 85)."""
    match = re.match(r'^(\d{1,3})_', filename)
    if match:
        score = int(match.group(1))
        return score if 0 <= score <= 100 else None
    return None


def get_url_from_filename(filename: str) -> str:
    """Convert filename to a readable URL path."""
    # Remove score prefix and extension
    name = re.sub(r'^\d{1,3}_', '', filename)
    name = name.replace('.json', '')
    # Convert underscores back to slashes
    url = '/' + name.replace('_', '/')
    return url


def truncate_text(text: str, max_length: int = 100) -> str:
    """Truncate text with ellipsis if too long."""
    if len(text) <= max_length:
        return text
    return text[:max_length - 3] + "..."


def escape_xml(text: str) -> str:
    """Escape XML special characters for ReportLab Paragraphs."""
    if not isinstance(text, str):
        text = str(text)
    return (text
            .replace('&', '&amp;')
            .replace('<', '&lt;')
            .replace('>', '&gt;')
            .replace('"', '&quot;')
            .replace("'", '&#39;'))


def get_score_bucket(score: int) -> Tuple[str, Color]:
    """Get the bucket label and color for a score."""
    if score >= 85:
        return 'excellent', SCORE_COLORS['excellent']
    elif score >= 70:
        return 'good', SCORE_COLORS['good']
    elif score >= 50:
        return 'needs_work', SCORE_COLORS['needs_work']
    else:
        return 'poor', SCORE_COLORS['poor']


