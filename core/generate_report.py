class ScoreGauge(Flowable):
    """Circular score gauge for executive summary."""
    
    def __init__(self, score: int, size: float = 100, primary_color: Color = None):
        Flowable.__init__(self)
        self.score = score
        self.size = size
        self.primary_color = primary_color or HexColor(DEFAULT_PRIMARY_COLOR)
        self.width = size
        self.height = size
    
    def draw(self):
        # Background circle (gray)
        self.canv.setStrokeColor(HexColor("#e5e7eb"))
        self.canv.setLineWidth(8)
        self.canv.circle(self.size/2, self.size/2, self.size/2 - 10)
        
        # Score arc
        _, score_color = get_score_bucket(self.score)
        self.canv.setStrokeColor(score_color)
        self.canv.setLineWidth(8)
        
        # Draw arc from 90 degrees (top) counter-clockwise
        start_angle = 90
        extent = -3.6 * self.score  # 360 degrees = 100%
        
